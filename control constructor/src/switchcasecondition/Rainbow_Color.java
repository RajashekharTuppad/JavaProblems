package switchcasecondition;

import java.util.Scanner;

public class Rainbow_Color {

	public static void main(String[] args) {
		Scanner scan=new Scanner(System.in);
		System.out.println("Enter the Character");
		char color=scan.next().charAt(0);
		
		scan.close();
		
	switch(color){
	case'v':
	System.out.println("The Rainbow color ");
	break;

	case'i':
		System.out.println("The Rainbow color ");
		break;

	
	case'g':
		System.out.println("The Rainbow color ");
		break;

	
	case'y':
		System.out.println("The Rainbow color ");
		break;

	
	case'o':
		System.out.println("The Rainbow color ");
		break;

	
	case'r':
		System.out.println("The Rainbow color ");
		break;

	
	default:
		System.out.println("The Rainbow color ");
		break;

	}
	
}

	}


