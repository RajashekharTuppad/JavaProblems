package com.kd.Abstraction_CarExample;

 public class ElectricalCar extends Car {

	public void start() {
		System.out.println(" Electrical Car is Started");
	}
	
	public void stop() {
		System.out.println(" Electrical Car is Stoped");
	}
	
	public void refuel() {
		System.out.println("Electrical Car is Recharged");
		
	}

}
