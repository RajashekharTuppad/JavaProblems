package com.kn.ArrayTasks;

public class ArrayTraversingBack {

	
		public void printArrayElements(int[] arr) {
			System.out.println("**********************");
			System.out.println("Array Elements as Follows:");
			for(int i=arr.length-1;i>=0;i--){
				System.out.println(arr[i]+" ");
			}
		}

	}

	
